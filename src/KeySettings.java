public class KeySettings {
    private static String rightKey = "RIGHT";
    private static String downKey = "DOWN";
    private static String leftKey = "LEFT";
    private static String upKey = "UP";

    public static String getRightKey() {
        return rightKey;
    }

    public static void setRightKey(String key) {
        rightKey = key;
    }

    public static String getDownKey() {
        return downKey;
    }

    public static void setDownKey(String key) {
        downKey = key;
    }

    public static String getLeftKey() {
        return leftKey;
    }

    public static void setLeftKey(String key) {
        leftKey = key;
    }

    public static String getUpKey() {
        return upKey;
    }

    public static void setUpKey(String key) {
        upKey = key;
    }
}